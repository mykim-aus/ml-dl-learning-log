# 2-layer neural network from scratch (NumPy)
# 순전파 + 역전파를 손으로 직접 구현해보기
# KIM이 질문에 답하며 한 줄씩 채워나가는 파일

import numpy as np

# 1. 데이터 준비 (XOR)
#    행(세로) = 샘플 개수, 열(가로) = 특징 개수
X = np.array([[0, 0],
              [0, 1],
              [1, 0],
              [1, 1]])   # (4, 2): 샘플 4개, 각 샘플의 특징 2개

y = np.array([[0],
              [1],
              [1],
              [0]])      # (4, 1): 샘플마다 정답 1개

# 2. 가중치 초기화
#    세트(뉴런) 하나 = 세로 열 하나. 세트끼리 다르게 하려고 랜덤으로 시작.
np.random.seed(42)              # 재현성: 매번 같은 랜덤값이 나오게 고정
W1 = np.random.randn(2, 8)      # (2, 8): 입력 2개 → 은닉 뉴런(세트) 8개
b1 = np.zeros((1, 8))           # (1, 8): 뉴런마다 편향 1개. 0으로 시작(대칭은 W1이 이미 깸)
W2 = np.random.randn(8, 1)      # (8, 1): 은닉 8개 → 출력 뉴런 1개
b2 = np.zeros((1, 1))           # (1, 1): 출력 뉴런 1개의 편향

# 3. 순전파 (forward pass)
z1 = X @ W1 + b1                # (4, 8): 1층 가중합. X@W1=(4,8), b1은 브로드캐스팅으로 더해짐
a1 = np.maximum(0, z1)          # (4, 8): ReLU. 각 값과 0 중 큰 쪽 → 음수는 0, 양수는 그대로
z2 = a1 @ W2 + b2               # (4, 1): 2층 가중합. a1@W2=(4,1), b2는 브로드캐스팅으로 더해짐
output = 1 / (1 + np.exp(-z2))  # (4, 1): sigmoid. z2를 0~1 확률로 찌부러뜨림 → 최종 예측

