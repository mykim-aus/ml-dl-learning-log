#  [가중치]
#  W1     (2,8) 1층 가중치 = [[ 0.50 -0.14  0.65  1.52 -0.23 -0.23  1.58  0.77]
#                            [-0.47  0.54 -0.46 -0.47  0.24 -1.91 -1.72 -0.56]]
